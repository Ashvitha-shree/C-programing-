#include <stdio.h>
void main()
{
    int a=5;
    printf("%d\n",+a);//unary plus
    printf("%d\n",-a);//unary minus
    printf("%d\n",++a);// Increment: Pre increament
    printf("%d\n",a++);//Increment: post increament
    printf("%d\n",--a);//Decrement:Pre decrement
    printf("%d\n",a--);//Decrement:Post decrement



    int b=5;
    printf("%d\n",++b);// Increment: Pre increament
    printf("%d\n",b++);//Increment: post increament
    printf("%d\n",b);
    int  c=5;
    printf("%d\n",--c);//Decrement:Pre decrement
    printf("%d\n",c--);//Decrement:Post decrement
    printf("%d\n",c);

}

